<h2>Kerala</h2>
<p>Kerala capital is Thiruvananthapuram. The date of the formation of Kerala is 1 November,1956. There are many festivals celebrated in Kerala Nadu, but the major festival is Nehru Trophy boat race (Aug), Thrissur Pooram (May), Onam festival cum tourism week (Aug-Sept), Pulikali (May).
Major attractions for tourists are Kovalam, Wayanad, Kannur, Thekkady, Kasargodu, Moonnar, Kumarakom, Alappuzha.Kerala, people, majorly knows one languages Malayalam. There are many rivers which go or start from Kerala. A few of them are Periyar, Bharathapuzha.
</p> 
<p>Kerala state animal, bird, flower, and tree are Elephant, Great Indian hornbill, Kanikonna or Indian laburnum or amaltas, Coconut respectively.
