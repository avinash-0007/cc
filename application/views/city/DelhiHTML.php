<h2>Delhi</h2>
<p>
India's capital is Delhi. The date of the formation of The National Capital Territory of Delh is 1 February 1992. 
There are many festivals celebrated in  Delhi, but major festivals are Diwali , Eid , Holi, Navratri and Dussehra, Durga Puja, Guru Nanak Jayanti, Lohri , Independence Day, Republic Day.
Major attractions for tourists in Delhi are Akshardham temple, National Gandhi Museum, Humayun's tomb, Qutub Minar ,Jantar Mantar of Delhi, Lotus temple, Red Fort and India Gate .
In Delhi, people majorly know four languages Hindi, English, Punjabi, Urdu.
Yamuna is the only river which goes from Delhi.
</p>
<p>
Delhi state animal and bird are Nilgai, House sparrow respectively.</p>
